<p class="despre"> Initiativa cu New York-ul a venit datorita faptului ca apreciem modelul american in ceea ce priveste arhitectura. Noi, echipa YorkField, suntem de parere ca New York-ul este unul din cele mai frumoase orase,cu elemente care variaza de la skycrapere pana la case traditionale americane. Cartierele americane sunt construite liniar, deoarece majoritatea caselor impart aceeasi linie de design. Gardurile sunt aproape inexistente in cartierele americane, ceea ce ne duce cu gandul la rata scazuta a spargerilor. </p>
